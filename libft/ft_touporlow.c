#include "libft.h"

int		ft_toupper(int ch)
{
	if (ch >= 97 && ch <= 122)
		return (ch -= 32);
	return (ch);
}

int		ft_tolower(int ch)
{
	if (ch >= 65 && ch <= 90)
		return (ch += 32);
	return (ch);
}
